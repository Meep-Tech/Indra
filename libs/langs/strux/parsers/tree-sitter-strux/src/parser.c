      if (lookahead == '-') ADVANCE(19);
      if (lookahead == '\t' ||
          lookahead == ' ') ADVANCE(14);
      if (('0' <= lookahead && lookahead <= '9')) ADVANCE(17);
      if (lookahead != 0) ADVANCE(20);
      END_STATE();
    case 15:
      ACCEPT_TOKEN(aux_sym__inline_empty_token1);
      if (lookahead == '\n') ADVANCE(1);
